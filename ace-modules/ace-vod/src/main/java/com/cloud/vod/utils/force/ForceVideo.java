package com.cloud.vod.utils.force;

public class ForceVideo {
	private String id;
	private String name;
	private String url;
	private String p2pUrl;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getP2pUrl() {
		return p2pUrl;
	}

	public void setP2pUrl(String p2pUrl) {
		this.p2pUrl = p2pUrl;
	}

}
